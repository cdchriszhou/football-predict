def rank_to_abilities(rank: int) -> dict:
    """Derive approximate team abilities from FIFA rank for backtesting."""
    base = max(52.0, min(92.0, 92.0 - (rank - 1) * 0.42))
    return {
        "attack": round(base + (3 if rank <= 10 else 0)),
        "defend": round(base + (2 if rank <= 15 else -1)),
        "midfield": round(base),
        "speed": round(min(90, base + 2)),
        "physical": round(min(90, base - 1)),
        "tactic": "全攻全守",
    }


def match_to_team_dict(name: str, rank: int) -> dict:
    stats = rank_to_abilities(rank)
    return {"name": name, "rank": rank, **stats}
