def normalize_position(raw: str) -> str:
    """Normalize any position format to Chinese (门将/后卫/中场/前锋)."""
    if raw in POSITION_MAP_TO_CN:
        return POSITION_MAP_TO_CN[raw]
    upper = raw.upper().strip()
    if upper in POSITION_MAP_TO_CN:
        return POSITION_MAP_TO_CN[upper]
    logger.warning(f"Unknown position format: '{raw}', defaulting to 中场")
    return "中场"


def _resolve_team_en(wiki_name: str) -> str:
    """Resolve a Wikipedia team name to our canonical English name."""
    if wiki_name in EN_TO_CN_TEAM:
        return wiki_name
    if wiki_name in WIKIPEDIA_NAME_ALIASES:
        return WIKIPEDIA_NAME_ALIASES[wiki_name]
    return wiki_name


def _derive_team_abilities(points: float) -> dict:
    """Derive team ratings from FIFA ranking points (deterministic, no random noise)."""
    base = 50.0 + (points - 1200.0) * (45.0 / 700.0)
    base = max(55.0, min(95.0, base))

    def slot(offset: float) -> int:
        return int(max(50, min(99, round(base + offset))))

    return {
        "attack": slot(2),
        "defend": slot(-1),
        "midfield": slot(0),
        "speed": slot(3),
        "physical": slot(1),
    }


def _derive_player_ability(
    team_rank: int,
    position: str,
    is_starter: bool,
    player_index: int,
    age: int,
) -> int:
    """Derive individual player ability (0-100) from team rank, position, role, age."""
    if team_rank <= 5:
        base = 85
    elif team_rank <= 10:
        base = 80
    elif team_rank <= 20:
        base = 75
    elif team_rank <= 40:
        base = 68
    else:
        base = 60

    pos_mod = {"门将": -5, "后卫": -2, "中场": 0, "前锋": 3}
    base += pos_mod.get(position, 0)
    if not is_starter:
        base -= 10

    if player_index <= 11:
        pass
    elif player_index <= 16:
        base -= 3
    elif player_index <= 20:
        base -= 6
    else:
        base -= 9

    if 25 <= age <= 30:
        base += 2
    elif age >= 37:
        base -= 6
    elif age >= 34 or age <= 20:
        base -= 3

    return max(40, min(97, int(base)))


def merge_team_data(
    cn_name: str,
    scraped_players: Optional[list],
    team_info: dict,
) -> tuple[list[dict], str]:
    """Merge scraped Wikipedia data with derived ability scores.

    Returns (players_list, source_tag) where source_tag is "scraped" or "fallback".
    Each player in the list is a dict ready for DB insertion.
    """
    if scraped_players and len(scraped_players) >= 11:
        players = []
        for i, sp in enumerate(scraped_players):
            position = normalize_position(sp.get("position", ""))
            age_val = sp.get("age") or 25
            ability = _derive_player_ability(
                team_info["rank"], position,
                i < 11, i + 1, age_val,
            )
            players.append({
                "name": sp.get("name", ""),
                "name_en": sp.get("name", ""),
                "position": position,
                "number": sp.get("number", i + 1),
                "age": age_val,
                "ability": ability,
                "is_starter": 1 if i < 11 else 0,
                "club": sp.get("club", ""),
            })
        return players, "scraped"

    fallback = PLAYER_DATA.get(cn_name, [])
    players = []
    for i, (pname, pos_cn, age_val, ab, starter) in enumerate(fallback):
        players.append({
            "name": pname,
            "name_en": pname,
            "position": normalize_position(pos_cn),
            "number": i + 1,
            "age": age_val,
            "ability": ab,
            "is_starter": starter,
            "club": "",
        })
    return players, "fallback"


