async def _upsert_team(
    db: AsyncSession,
    cn_name: str,
    group_name: str,
    team_info: dict,
    players: list[dict],
) -> tuple[int, str]:
    """Upsert a team and its players. Returns (player_count, action).

    action is "created" if the team is new, "updated" if it already existed.
    """
    existing_team = (
        await db.execute(
            select(Team).where(Team.name == cn_name, Team.competition_slug == "worldcup-2026")
        )
    ).scalar_one_or_none()

    abilities = _derive_team_abilities(team_info["points"])

    if existing_team:
        team = existing_team
        action = "updated"
        team.rank = team_info["rank"]
        team.attack = abilities["attack"]
        team.defend = abilities["defend"]
        team.midfield = abilities["midfield"]
        team.speed = abilities["speed"]
        team.physical = abilities["physical"]
        team.tactic = team_info["tactic"]
        team.price = team_info["price"]
        team.group_name = group_name
        team.competition_slug = "worldcup-2026"
        await db.execute(delete(Player).where(Player.team_id == team.id))
    else:
        action = "created"
        team = Team(
            competition_slug="worldcup-2026",
            name=cn_name,
            name_en=cn_name,
            flag_url="",
            rank=team_info["rank"],
            attack=abilities["attack"],
            defend=abilities["defend"],
            midfield=abilities["midfield"],
            speed=abilities["speed"],
            physical=abilities["physical"],
            tactic=team_info["tactic"],
            price=team_info["price"],
            group_name=group_name,
        )
        db.add(team)
        await db.flush()

    for p in players:
        status = _player_status(p["age"])
        player = Player(
            team_id=team.id,
            name=p["name"],
            name_en=p.get("name_en", p["name"]),
            position=p["position"],
            number=p["number"],
            age=p["age"],
            status=status,
            ability=p["ability"],
            is_starter=p["is_starter"],
        )
        db.add(player)

    return len(players), action


async def run_team_crawler(db: AsyncSession):
    """Scrape Wikipedia squad data with hardcoded fallback. Per-team upsert."""
    async with crawler_lock:
        start_time = datetime.now()
        stats = {
            "created": 0, "updated": 0,
            "players": 0, "scraped_teams": 0, "fallback_teams": 0,
        }

        try:
            # 1. Attempt Wikipedia scrape
            scraped = await scrape_all_squads()
            logger.info(f"Wikipedia scrape returned data for {len(scraped)} teams")

            # 2. Resolve Wikipedia team names to canonical English names
            resolved_scraped: dict[str, list[dict]] = {}
            for wiki_name, squad in scraped.items():
                canonical = _resolve_team_en(wiki_name)
                resolved_scraped[canonical] = squad
                if canonical != wiki_name:
                    logger.info(f"  Resolved Wikipedia name '{wiki_name}' -> '{canonical}'")

            # 3. Per-team upsert
            for group_name, team_names in GROUPS.items():
                for cn_name in team_names:
                    info = TEAM_DATA.get(cn_name)
                    if info is None:
                        logger.warning(f"No TEAM_DATA entry for '{cn_name}', skipping")
                        continue

                    en_name = TEAM_NAME_MAP.get(cn_name, cn_name)
                    wiki_players = resolved_scraped.get(en_name)

                    merged_players, source = merge_team_data(cn_name, wiki_players, info)

                    if source == "scraped":
                        stats["scraped_teams"] += 1
                    else:
                        stats["fallback_teams"] += 1

                    player_count, action = await _upsert_team(
                        db, cn_name, group_name, info, merged_players
                    )
                    stats[action] += 1
                    stats["players"] += player_count

            await db.flush()

            summary = (
                f"Teams: {stats['scraped_teams']} scraped, {stats['fallback_teams']} fallback | "
                f"Created {stats['created']}, updated {stats['updated']} | "
                f"Players: {stats['players']}"
            )
            await _log_crawler(db, "team", "success", stats["players"], start=start_time)
            logger.info(f"Team crawler complete: {summary}")
            return {"status": "success", **stats, "summary": summary}

        except Exception as e:
            await _safe_crawler_fail(db, "team", e, start_time)
            return {"status": "failed", "error": str(e)}
